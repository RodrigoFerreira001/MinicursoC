#include <stdio.h>

int main(int argc, char *argv[]){

	//Declaração de variáveis
	int a = 10;
	int b = 20;

	printf("a = %d\n", a);
	printf("b = %d\n\n", b);

	/*

	Isso é um comentário de bloco!

	*/

	printf("a + b = %d\n", a + b);
	printf("a - b = %d\n", a - b);
	printf("a / b = %d\n", a / b);
	printf("a * b = %d\n", a * b);
	printf("a %% b = %d\n", a % b); // Resto da divisão de a por b

	return 0;
}
